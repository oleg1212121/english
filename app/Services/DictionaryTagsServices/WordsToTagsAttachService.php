    const CHUNK_SIZE = 500;

    public function __construct(DictionaryTag $tag = null)
    {
        $this->tag = $tag;
    }

    public function attach(array $arr = [])
    {
        $this->tag->words()->syncWithoutDetaching($arr);
    }

